                    <p>
The Cranberries were an Irish rock band formed in Limerick, Ireland. Originally named the Cranberry Saw Us, the band was established in 1989 by lead singer Niall Quinn, guitarist Noel Hogan, bassist Mike Hogan, and drummer Fergal Lawler. Quinn was later replaced as the lead singer by Dolores O'Riordan in 1990, prompting the group to change their name to the Cranberries. They classified themselves as an alternative rock group but incorporated elements of indie rock, jangle pop, dream pop, folk rock, post-punk, and pop rock into their sound.</p>
                    <br>
                    <p>
                        The Cranberries achieved international fame in the 1990s with their debut album, "Everybody Else Is Doing It, So Why Can't We?," which became a commercial success. Some of their notable hit singles include "Dreams" (1992), "Linger" (1993), "Zombie" (1994), "Salvation" (1996), and "When You're Gone" (1997). Five of their albums reached the Top 20 on the Billboard 200 chart, and eight of their singles reached the Top 20 on the Modern Rock Tracks chart.

After a six-year hiatus, the Cranberries reunited in early 2009 and embarked on a North American tour followed by shows in Latin America and Europe. They released their sixth studio album, "Roses," in February 2012, and the seventh, "Something Else," followed in April 2017.
